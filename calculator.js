function add(operand1, operand2) {
  return Number(operand1) + Number(operand2);
}

function subtract(operand1, operand2) {
  return Number(operand1) - Number(operand2);
}

function multiply(operand1, operand2) {
  return Number(operand1) * Number(operand2);
}

function divide(operand1, operand2) {
  return Number(operand1) / Number(operand2);
}